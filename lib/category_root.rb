  def self.update_beer_category_root(overwrite = false)
    Trainer.new.unrated_beers.each do |beer|
      next if beer.category_root_name.present? && !overwrite
      style = beer.style
      next unless style
      beer.category_root_name = mappings[style.name]
      beer.save!
    end
    true
  end
